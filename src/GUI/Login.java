    private boolean searchPlayer(String onePlayerName, String twoPlayerName) {
        for (int i = 0; i < player.twoPlayers.size(); i++) {
            if (player.twoPlayers.get(i).getName().equals(onePlayerName) && player.twoPlayers.get(i).getName().equals(twoPlayerName)) {
                return true;
            }
        }
        return false;
    }

    private boolean search1p(String onePlayerName) {
        for (int i = 0; i < player.onePlayer.size(); i++) {
            if (player.onePlayer.get(i).getName().equals(onePlayerName)) {
                return true;
            }
        }
        return false;
    }

