    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed

        try {
            String name = JOptionPane.showInputDialog("Escriba su nombre");
            if (name.isEmpty()) {
                JOptionPane.showMessageDialog(null, "Digite un texto válido");
            } else {
                player = new Players();
                player.setName(name);
                player.onePlayer.add(player);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "No hay datos para registrar");
        }
    }//GEN-LAST:event_btnLoginActionPerformed

    private void btnOnePlayerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOnePlayerActionPerformed
        Players playerOne = new Players();
        if (player.onePlayer.isEmpty()) {
            JOptionPane.showMessageDialog(null, "No hay jugadores disponibles, regístrese");
        } else {
            String onePlayerName = JOptionPane.showInputDialog(null, "¿Cúal es su nombre?");
            if (onePlayerName.isEmpty() && onePlayerName != null) {
                playerOne.setName(onePlayerName);
                player.onePlayer.add(playerOne);
            }
            if (search1p(onePlayerName)) {
                JFOthelloPrincipal window = new JFOthelloPrincipal();
                window.setVisible(true);
                this.setVisible(false);
            } else {
                JOptionPane.showMessageDialog(null, "No se encontró su nombre");
            }
    }//GEN-LAST:event_btnOnePlayerActionPerformed
    }
    private void btnTwoPlayersActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTwoPlayersActionPerformed
        Players player1 = new Players();
        Players player2 = new Players();

        String input = JOptionPane.showInputDialog("Escriba su nombre");
        if (input.isEmpty() && input != null) {
            if (input.isEmpty() && input != null) {
                JOptionPane.showMessageDialog(null, "Digite un texto válido");
            } else {
                String onePlayerName = JOptionPane.showInputDialog("Escriba su nombre jugador 1");
                player1.setName(onePlayerName);
                String twoPlayerName = JOptionPane.showInputDialog("Escriba su nombre jugador 2");
                player2.setName(twoPlayerName);
                if (searchPlayer(onePlayerName, twoPlayerName)) {
                    JFOthelloPrincipal window = new JFOthelloPrincipal();
                    window.setVisible(true);
                    this.setVisible(false);
                } else {
                    JOptionPane.showMessageDialog(null, "No se encontraron sus datos");
                }
            }
        } else {
            JOptionPane.showMessageDialog(null, "No se encontraron sus datos");
        }
    }//GEN-LAST:event_btnTwoPlayersActionPerformed

